TITLE   8BCDTOBIN
PAGE    50,100
NOFF=	100H
RESOFF=	200H	
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR	
	PUSH DS
	MOV AX,0
	PUSH AX		
	mov SI,NOFF
	MOV AL,[SI+3]
	MOV CH,07H
	MOV CL,04H
	SHR AL,CL
LOP:	MOV BX,0AH;AX IS BASE FOR MULTI ALWAYS AX*10+BX WHERE BX IS SHIFTED VALUE
	MOV DI,DX;FIRST AX*10 THEN DX*10 THEN MOVE AX TO DX ADD BOTH DX
	MUL BX
	MOV SI,AX
	MOV AX,DI
	MOV DI,DX
	MUL BX
	MOV DX,AX
	MOV AX,SI
	ADD DX,DI
	MOV SI,NOFF
	CMP CH,04H
	JE QWE
	JB SAD
	SAL WORD PTR [SI+2],CL
 	MOV BL,[SI+3]
	SHR BL,CL
	JMP STE
QWE:	MOV BL,[SI+1]	
	SHR BL,CL
	JMP STE
SAD:	SAL WORD PTR [SI],CL
 	MOV BL,[SI+1]
	SHR BL,CL
STE:	ADD AX,BX
	ADC DX,0
	DEC CH
	JNZ LOP
	MOV SI,RESOFF
	MOV [SI],AX
	MOV [SI+2],DX
	RET
MAIN 	ENDP
CODE_S	ENDS
END	MAIN	