TITLE    DEC_ADD
PAGE     50,100
N1OFF=	0H
N2OFF=	100H
HCFOFF=	200H
LCMOFF=	250H
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV SI,N1OFF
	MOV AX,[SI]
	MOV SI,N2OFF
	MOV BX,[SI]
	PUSH AX
	PUSH BX
	CALL HCF
	MOV DI,HCFOFF
	MOV[DI],BX
	POP BX
	POP AX
	MUL BX
	MOV BX,[DI]
	DIV BX
	MOV DI,LCMOFF
	MOV [DI],AX
	RET
MAIN	ENDP
HCF	PROC
	CMP AX,BX
	JB REV
	MOV DX,0
	DIV BX
	CMP DX,0
	JE FINAL
	MOV AX,DX
	CALL HCF
	JMP FINAL
REV:	XCHG AX,BX
	CALL HCF
FINAL:	RET
HCF	ENDP
CODE_S	ENDS
END	MAIN			