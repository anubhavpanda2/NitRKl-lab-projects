TITLE    SQRT
PAGE     50,100
NOFF=	100H
RESOFF=	200H
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV SI,NOFF
	MOV AX,[SI]
	MOV DI,AX
	MOV CL,AL
LOP:	XOR CH,CH
	MOV AX,CX	
	MOV BL,02H
	DIV BL
	XCHG AL,CL
	MUL BL
	MOV BX,AX
	MOV AX,DI
	XOR DX,DX
	DIV BX
	ADD CX,AX
	MOV AL,CL
	MUL AL
	MOV BX,AX
	MOV AX,DI
	SUB AX,BX
	CMP AX,CX
	JLE LOP
	MOV SI,RESOFF
	MOV [SI],CL
	RET
MAIN	ENDP
CODE_S	ENDS
END 	MAIN	
	
	
	
		