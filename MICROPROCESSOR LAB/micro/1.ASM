TITLE    FN2
PAGE     50,100
ASSUME  CS:CODE_S,DS:DATA_S      
SPCHOF=	200H
DATA_S	SEGMENT	'DATA'
SEN	DB	"Rama IS GOOD boy."
DATA_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DX
	MOV AX,0
	PUSH AX
	MOV CL,0
	MOV AX,SEG SEN
	MOV DS,AX
	MOV SI,OFFSET SEN
Start:	CMP BYTE PTR [SI],2EH
	JZ ASD
	INC SI
	INC CL
	JMP Start
ASD:	MOV DX,CX
	CLD
	MOV DI,0
	DEC SI
FORM:	MOV AL,[SI]
	MOV ES:[DI],AL
	DEC SI
	INC DI
	LOOP FORM
	XOR SI,SI
	XOR DI,DI
	MOV CX,DX
	REPZ CMPSB
	CMP CX,0
	JZ RES
	JMP SAP
RES:	MOV SI,SPCHOF
	MOV BYTE PTR [SI],40H	
SAP:	RET
MAIN	ENDP
CODE_S	ENDS
END	MAIN