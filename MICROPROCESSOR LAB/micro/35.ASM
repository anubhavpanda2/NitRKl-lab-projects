TITLE    DEC_ADD
PAGE     50,100
N1OFF=	0H
N2OFF=	100H
RESOFF=	200H
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV SI,N1OFF
	MOV DI,N2OFF
	MOV AL,[SI]
	AND AL,0FH
	CALL PRO
	PUSH DX
	PUSH AX
	MOV AL,[SI]
	SHR AL,CL
	CALL PRO
	POP BX
	MOV AL,DH
	ADD AL,BH
	DAA
	MOV DH,AL
	POP BX
	MOV AL,DL
	ADD AL,BH
	DAA
	MOV DL,AL
	MOV CH,DH
	SHR CH,CL
	ADD AH,CH
	AND DH,0FH
	MOV CH,DL
	SHR CH,CL
	ADD DH,CH
	AND DL,0FH
	SHL AH,CL
	ADD AH,DH
	SHL DL,CL
	ADD DL,BL
	MOV AL,DL
	MOV DI,RESOFF
	MOV [DI],AX
	RET
MAIN	ENDP
PRO	PROC
	MOV CH,AL
	MOV BL,[DI]
	AND BL,0FH
	MUL BL
	AAM
	MOV DX,AX
	MOV BL,[DI]
	MOV AL,CH
	AND BL,0F0H
	MOV CL,04H
	SHR BL,CL
	MUL BL
	AAM
	ADD AL,DH
	DAA
	MOV BL,AL
	AND AL,0FH
	MOV DH,AL
	SHR BL,CL
	ADD AH,BL		
	RET
PRO	ENDP
CODE_S	ENDS
END	MAIN	