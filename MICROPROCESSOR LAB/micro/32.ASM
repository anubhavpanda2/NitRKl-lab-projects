TITLE    REM_DIVF
PAGE     50,100
N1OFF=	100H
RESOFF=	200H
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV SI,N1OFF
	MOV DI,RESOFF
	MOV DX,[SI]
	MOV CL,10H
	XOR BL,BL
	CALL SUM
	MOV DX,[SI+2]
	MOV CL,10H
	CALL SUM
	MOV AL,BL
	MOV AH,0
	MOV BL,0FH
	DIV BL
	MOV BYTE PTR [DI],AH
	RET
MAIN	ENDP	
SUM	PROC
LOP:	SUB CL,4
	MOV AX,DX
	SHR AX,CL
	AND AL,0FH	
	ADD BL,AL
	CMP CL,0
	JNZ LOP
	RET
SUM	ENDP
CODE_S	ENDS
END	MAIN
	
