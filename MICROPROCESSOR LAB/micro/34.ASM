TITLE    DEC_ADD
PAGE     50,100
N1OFF=	0H
N2OFF=	100H
RESOFF=	200H
SPCH=	250H
ASSUME  CS:CODE_S,SS:STACK_S
STACK_S	SEGMENT	'STACK'
DB	64	DUP(?)
STACK_S	ENDS
CODE_S	SEGMENT	'CODE'
MAIN	PROC	FAR
	PUSH DS
	MOV AX,0
	PUSH AX
	MOV SI,N1OFF
	MOV Al,[SI]
	ADD al,DS:[N2OFF]
	DAA
	MOV DS:[RESOFF],AL
	MOV AL,[SI+1]
	ADC AL,DS:[N2OFF+1]
	DAA
	MOV DS:[RESOFF+1],AL
	JC ASD
	JMP FINISH
ASD:	MOV SI,SPCH
	MOV BYTE PTR [SI],40H
FINISH:	RET
MAIN	ENDP
CODE_S	ENDS
END	MAIN			